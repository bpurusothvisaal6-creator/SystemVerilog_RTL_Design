module carry_save_adder_4bit (

    input  logic [3:0] a,
    input  logic [3:0] b,
    input  logic [3:0] c,

    output logic [3:0] sum,
    output logic [4:0] carry

);

logic c0, c1, c2, c3;

//---------------------------------------------------------------------
// Bit 0
//---------------------------------------------------------------------

full_adder FA0 (

    .a   (a[0]),
    .b   (b[0]),
    .cin (c[0]),

    .sum (sum[0]),
    .cout(c0)

);

//---------------------------------------------------------------------
// Bit 1
//---------------------------------------------------------------------

full_adder FA1 (

    .a   (a[1]),
    .b   (b[1]),
    .cin (c[1]),

    .sum (sum[1]),
    .cout(c1)

);

//---------------------------------------------------------------------
// Bit 2
//---------------------------------------------------------------------

full_adder FA2 (

    .a   (a[2]),
    .b   (b[2]),
    .cin (c[2]),

    .sum (sum[2]),
    .cout(c2)

);

//---------------------------------------------------------------------
// Bit 3
//---------------------------------------------------------------------

full_adder FA3 (

    .a   (a[3]),
    .b   (b[3]),
    .cin (c[3]),

    .sum (sum[3]),
    .cout(c3)

);

//---------------------------------------------------------------------
// Carry Vector
//---------------------------------------------------------------------

always_comb begin

    carry[0] = 1'b0;
    carry[1] = c0;
    carry[2] = c1;
    carry[3] = c2;
    carry[4] = c3;

end

endmodule